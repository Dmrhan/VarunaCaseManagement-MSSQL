/**
 * Baseline migration hazırlayıcı (Faz 1).
 *
 * - scripts/mssql-baseline.sql içindeki 3 nullable-unique constraint'i
 *   filtered unique index'e çevirir (MSSQL multiple-NULL davranışı için).
 * - scripts/mssql-checks.sql içindeki CHECK constraint'leri sona ekler.
 * - prisma/migrations/00000000000000_init/migration.sql olarak yazar,
 *   migration_lock.toml (sqlserver) üretir.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const read = (p) => {
  const buf = fs.readFileSync(p);
  let text;
  if (buf[0] === 0xff && buf[1] === 0xfe) text = buf.toString('utf16le', 2);
  else if (buf[0] === 0xef && buf[1] === 0xbb && buf[2] === 0xbf) text = buf.toString('utf8', 3);
  else text = buf.toString('utf8');
  return text.replace(/\r\n/g, '\n');
};

let sql = read(path.join(root, 'scripts', 'mssql-baseline.sql'));

// Nullable kolon içeren tüm unique'ler: MSSQL'de Postgres'in "çoklu NULL"
// davranışını korumak için filtered index olmalı.
const FILTERED = [
  { name: 'CaseSolutionStep_caseId_source_sourceRef_key', table: 'CaseSolutionStep', filter: '[sourceRef] IS NOT NULL' },
  { name: 'NotificationDispatch_idempotencyKey_unique', table: 'NotificationDispatch', filter: '[idempotencyKey] IS NOT NULL' },
  { name: 'ActionItem_dedupKey_key', table: 'ActionItem', filter: '[dedupKey] IS NOT NULL' },
  { name: 'Account_tcknHash_key', table: 'Account', filter: '[tcknHash] IS NOT NULL' },
  { name: 'Account_vkn_key', table: 'Account', filter: '[vkn] IS NOT NULL' },
  { name: 'AccountCompany_companyId_externalCustomerCode_key', table: 'AccountCompany', filter: '[externalCustomerCode] IS NOT NULL' },
  { name: 'AccountProduct_accountCompanyId_productCode_key', table: 'AccountProduct', filter: '[productCode] IS NOT NULL' },
  { name: 'User_personId_key', table: 'User', filter: '[personId] IS NOT NULL' },
];

const appended = [];
for (const fx of FILTERED) {
  // İki biçimden biri olabilir: CREATE TABLE içi inline CONSTRAINT veya ALTER TABLE ADD CONSTRAINT
  const inlineRe = new RegExp(`\\n\\s*CONSTRAINT \\[${fx.name}\\] UNIQUE NONCLUSTERED \\(([^)]*)\\)(,?)`);
  const alterRe = new RegExp(`ALTER TABLE \\[dbo\\]\\.\\[${fx.table}\\] ADD CONSTRAINT \\[${fx.name}\\] UNIQUE NONCLUSTERED \\(([^)]*)\\);`);
  let cols = null;
  const mi = sql.match(inlineRe);
  const ma = sql.match(alterRe);
  if (mi) {
    cols = mi[1];
    sql = sql.replace(inlineRe, '');
    sql = sql.replace(/,(\n\);)/g, '$1'); // listenin sonundaysa sarkan virgülü temizle
  } else if (ma) {
    cols = ma[1];
    sql = sql.replace(alterRe, '');
  } else {
    throw new Error(`unique constraint not found in baseline: ${fx.name}`);
  }
  appended.push(
    `-- Filtered unique (MSSQL: NULL satırlar uniqueness dışı)\n` +
    `CREATE UNIQUE NONCLUSTERED INDEX [${fx.name}] ON [dbo].[${fx.table}](${cols}) WHERE ${fx.filter};`
  );
}

const checks = read(path.join(root, 'scripts', 'mssql-checks.sql')).trim();

sql = sql.trimEnd() +
  '\n\n-- ── Filtered unique indexes ──\n' + appended.join('\n') +
  '\n\n-- ── CHECK constraints (enum değer setleri) ──\n' + checks + '\n';

const migDir = path.join(root, 'prisma', 'migrations', '00000000000000_init');
fs.mkdirSync(migDir, { recursive: true });
fs.writeFileSync(path.join(migDir, 'migration.sql'), sql, 'utf8');
fs.writeFileSync(
  path.join(root, 'prisma', 'migrations', 'migration_lock.toml'),
  '# Please do not edit this file manually\n# It should be added in your version-control system (e.g., Git)\nprovider = "mssql"\n',
  'utf8'
);
console.log('baseline migration written:', path.join(migDir, 'migration.sql'));
console.log('lines:', sql.split('\n').length);
